function limeiyy() {
  var copyText = document.getElementById("limeiyy");
  copyText.select();
  copyText.setSelectionRange(0, 99999); 
  navigator.clipboard.writeText(copyText.value);
  alert("Name kopiert: " + copyText.value);
} 
